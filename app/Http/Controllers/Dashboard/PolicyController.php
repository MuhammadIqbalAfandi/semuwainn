    {
        try {
            Policy::truncate();
            Policy::create($request->validated());

            return back()->with('success', __('messages.success.store.policy'));
        } catch (QueryException $e) {
            dd($e);
            return back()->with('failed', __('messages.errors.store.all'));
        }
    }

